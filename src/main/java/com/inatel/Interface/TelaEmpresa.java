    public void adicionarEmpresa(){
        EmpresaDAO e = new EmpresaDAO();
        Empresa empresa;
        
        if("".equals(nomeEmpresaNova.getText()) || "".equals(senhaEmpresaNova.getText())){
            JOptionPane.showMessageDialog(null,"Preencha os campos corretamente");
        }else{
            empresa = new Empresa(nomeEmpresaNova.getText(),senhaEmpresaNova.getText());
            boolean inserirEmpresa = e.inserirEmpresa(empresa);
            if (inserirEmpresa == true){
                JOptionPane.showMessageDialog(null,"Nova empresa adicionado.");
            }
            TelaInicial t = new TelaInicial();
            this.setVisible(false);
            t.setVisible(true);
        }
    }
    
    public void verificarEmpresa(){
        EmpresaDAO empresaDAO = new EmpresaDAO();
        if("".equals(nomeEmpresaExistente.getText()) || "".equals(senhaEmpresaExistente.getText())){
            JOptionPane.showMessageDialog(null,"Preencha os campos corretamente");
        }else if(empresaDAO.buscarEmpresa(nomeEmpresaExistente.getText(), senhaEmpresaExistente.getText())== true){
            TelaInicial t = new TelaInicial();
            this.setVisible(false);
            t.setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(null,"Nome da empresa ou senha est√£o incorretos");
        }
    }

